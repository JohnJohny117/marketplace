//- Функция получения текущих географических координат пользователя через браузерный Geolocation API
//- Возвращает Promise, который при успехе разрешается с объектом { longitude, latitude },
//- а при ошибке или отсутствии поддержки – отклоняется с ошибкой.
function getCurrentPosition() {
  //- Создание нового Promise для асинхронного получения координат
  return new Promise((resolve, reject) => {
    //- Проверка наличия в браузере объекта navigator.geolocation
    if (!navigator.geolocation) {
      //- Отклонение Promise с сообщением о том, что геолокация не поддерживается
      reject(new Error('Геолокация не поддерживается вашим браузером'));
      return;
    }
    //- Вызов встроенного метода getCurrentPosition, который запрашивает у пользователя разрешение
    //- и асинхронно возвращает позицию или ошибку
    navigator.geolocation.getCurrentPosition(
      //- Колбэк при успешном получении координат
      (position) => {
        //- Извлечение широты и долготы из объекта position.coords
        //- и разрешение Promise с этими значениями
        resolve({
          longitude: position.coords.longitude, // Долгота (от -180 до 180)
          latitude: position.coords.latitude    // Широта (от -90 до 90)
        });
      },
      //- Колбэк при ошибке (пользователь отказал, таймаут и т.п.)
      (error) => {
        //- Отклонение Promise с полученной ошибкой
        reject(error);
      },
      //- Дополнительные опции для повышения точности и управления временем ожидания
      {
        enableHighAccuracy: true, // Запрос максимально точных данных (вкл. GPS)
        timeout: 10000,           // Максимальное время ожидания ответа (10 секунд)
        maximumAge: 0             // Не использовать кэшированные координаты старше 0 мс
      }
    );
  });
}